package com.ufc.mob.model;

public class Contate {
    public String name;
    public String email;
    public String whatsApp;
    public String genere;
    public String idade;

    public Contate(String name, String email, String whatsApp,String genere, String idade) {
        this.name = name;
        this.email = email;
        this.whatsApp = whatsApp;
        this.genere = genere;
        this.idade = idade;
    }
}
